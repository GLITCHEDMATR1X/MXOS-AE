function dedupeGallery(items) {
  const seen = new Set();
  const out = [];
  for (const item of items || []) {
    const raw = String(item || '').trim();
    if (!raw) continue;
    const key = normalizeAssetKey(raw);
    if (seen.has(key)) continue;
    seen.add(key);
    out.push(raw);
  }
  return out;
}

